// Syntax:
if (condition) {
  // code to run if condition is true
} else {
  // code to run if condition is false
}


// ---------- if --------------
const age = 15;

if (age >= 18) {
  console.log("You are an adult.");
} else {
  console.log("You are not an Adult.");
}



// ----------else if--------------
const time = 14; // 2 PM

if (time < 12) {
  console.log("Good morning.");
} 
else if (time < 18) {
  console.log("Good afternoon.");
} 
else {
  console.log("Good evening.");
}

